public class TestMinMax {

   private int min;
   private int max;
   private String name;

    public TestMinMax(int min, int max, String name) {
        this.min = min;
        this.max = max;
        this.name = name;
    }

    public int getMin() {
        return min;
    }

    public int getMax() {
        return max;
    }

    public String getName() {
        return name;
    }
}
